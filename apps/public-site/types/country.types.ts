// Reverse mapping: country code -> country name (for display when DB stores code)
const codeToNameMapping: Record<string, string> = {};
Object.entries(countryCodeMapping).forEach(([name, code]) => {
  codeToNameMapping[code] = name;
});

// Helper function to get country code from country name
export function getCountryCode(countryName: string): string {
  const code = countryCodeMapping[countryName];
  if (!code) {
    // If no match found, try to find a partial match
    const key = Object.keys(countryCodeMapping).find(k => 
      countryName.toLowerCase().includes(k.toLowerCase()) || 
      k.toLowerCase().includes(countryName.toLowerCase())
    );
    return key ? countryCodeMapping[key] : "UN"; // UN as fallback for unknown
  }
  return code;
}

// Helper function to get full country name from code or name (for consistent display)
export function getCountryDisplayName(value: string): string {
  if (!value) return "";
  const trimmed = value.trim();
  // If it's a 2-letter code (e.g. "US", "GB"), look up full name
  if (trimmed.length === 2 && /^[A-Za-z]{2}$/.test(trimmed)) {
    const upper = trimmed.toUpperCase();
    return codeToNameMapping[upper] ?? trimmed;
  }
  // Otherwise assume it's already a country name (possibly from legacy data)
  return trimmed;
}

// Helper to get country code for flag display (accepts either code or name)
export function getCountryCodeForDisplay(value: string): string {
  if (!value) return "UN";
  const trimmed = value.trim();
  // If it's a 2-letter code, use it directly (normalize to uppercase)
  if (trimmed.length === 2 && /^[A-Za-z]{2}$/.test(trimmed)) {
    return trimmed.toUpperCase();
  }
  return getCountryCode(trimmed);
}

// Helper function to format country name
export function formatCountryName(name: string): string {
  return name
    .split(/[\s-]+/)
    .map(word => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
    .join(" ");
} 